<h2>1. Nun-Sakeenah</h2>
<p> Nun-Sakeenah is fibngobingobnbiosdfnboinionioavnoibnioagni there are 5</p>

<div>
<h5> 1.1 Ghunna (Nasalisation)</h5>
<p>  คือ การอ่านที่ชัดเจน หมายถึง การอ่านที่ชัดเจนโดยที่ไม่หน่วงเสียง อ่านชัดอักษรนูนตาย สระซ้อนสอง และชัดอักษรอิซฮารทั้งหมดตัวอย่าง เช่น </p>
</div>

<div>
<h5> 1.2 Ikhfa'a (Lenition/Hiding)</h5>
<p>
    คือ การอ่านเข้ากัน (กล้ำกัน) พร้อมหน่วงเสียง
</p>
</div>

<div>
<h5> 1.3 Idgham (Elision/Merging)</h5>
<p>
    คือ การอ่านเข้ากัน (กล้ำกัน) โดยที่ไม่หน่วงเสียง
</p>
</div>

<div>
<h5> 1.4 Idgham without Ghunna</h5>
<p>
    คือ การอ่านก้ำกึ่ง หรืออ่านซ่อนเสียง หรือการอ่านครึ่งของนูน หรือตันวีด และไปพบกับอักษรอิคฟาอ์พร้อมกับหน่วงเสียงเข้ากัน (กล้ำกัน) โดยไม่หน่วงเสียง
</p>
</div>

<div>
<h5> 1.5 Iglab (Assimilation/Flipping)</h5>
<p>
    คือ การอ่านเปลี่ยนเสียงพร้อมทั้งหน่วงเสียง หมายถึง การอ่านหน่วงเสียงของอักษรนูนตาย หรือ ตันวีน ให้เป็นเสียงอักษรมีม (ห้ามปิดปากมิดชิดระหว่างออกเสียงมีม) แล้วไปพบกับอักษร บา พร้อมกับหน่วงเสียง
</p>
</div>



